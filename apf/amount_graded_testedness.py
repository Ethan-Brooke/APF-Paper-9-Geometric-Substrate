def _solve_consistency(g, rho, lo=1e-12, hi=1e9, iters=300):
    """Bisection solve of g(e) = rho for strictly increasing g (audit fix
    F5b: the par solution is COMPUTED, not assigned-then-asserted)."""
    lo, hi = float(lo), float(hi)
    for _ in range(iters):
        mid = 0.5 * (lo + hi)
        if g(mid) < rho:
            lo = mid
        else:
            hi = mid
    return 0.5 * (lo + hi)


